#include "libft.h"

int	ft_isnumber(char *nbr)
{
	int i;

	i = 0;
	while (nbr[i])
	{
		if (i == 0 && (nbr[0] == '-' || nbr[0] == '+'))
			i++;
		if (ft_isdigit(nbr[i]) == 0)
			return (0);
		i++;
	}
	return (1);
}
